# Database connection
DSN = "postgresql://citadel:citadel@127.0.0.1:5432/citadel_test"


def manual_calculate_hash(action_id, event_type, event_ts, payload_json, prev_hash, actor_id):
    """
    Manually calculate what the hash should be based on the trigger logic.
    
    Trigger logic:
        encode(
            digest(
                COALESCE(action_id::text, '') || 
                event_type || 
                event_ts::text || 
                COALESCE(payload_json::text, '{}') ||
                prev_hash ||
                COALESCE(actor_id, ''),
                'sha256'
            ),
            'hex'
        )
    """
    data = (
        str(action_id) if action_id else ""
    ) + event_type + str(event_ts) + json.dumps(payload_json) + prev_hash + (actor_id or "")
    return hashlib.sha256(data.encode("utf-8")).hexdigest()


